// 1- Single Line Comments
// Single line comments are used to comment a single line of code. 
// They are denoted by //. Everything after // is a comment and will not be executed by the computer.

// This is a single line comment
console.log("Hello World"); // This is also a single line comment


// 2-Multi-Line Comments:

// Multi-line comments are used to comment multiple lines of code. 
// They are denoted by /* and */. 
// Everything between /* and */ is a comment and will not be executed by the computer.


/*
This is 
a multi-line 
comment.
*/